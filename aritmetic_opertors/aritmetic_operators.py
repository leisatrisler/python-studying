# Aritmetic operators in Python are the same we have in Math
# We can add numbers, subtract them, multiple, etc
print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)  # Technically we have 2 different types of divison operators (/ gives us a floating number with a decimal point)
# Technically we have 2 different types of divison operators (// gives us an integer, whole number)
print(10 // 3)
print(10 ** 3)  # This is 10 to the power of 3

